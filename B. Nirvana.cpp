bool cmp(pair<int,int>a,pair<int,int> b)
{
    if(a.first > b.first)
        return true;
    else if(a.first == b.first and a.second<b.second)
        return true;

    return false;
}
const int N = 1e5+10;
bitset <N> vis;
void sieve (void)
{
    for (int i = 2; i * i < N; ++i)
    {
        if (!vis[i])
        {
            for (int j = i * i; j < N; j += i) vis[j] = 1;
        }
    }
}
ll max_pro(ll n)
{
    if(n==0)
    {
        return 1;
    }
    if(n<10)
    {
        return n;
    }
    return max(max_pro(n/10)*(n%10),max_pro((n/10)-1)*9);
}
